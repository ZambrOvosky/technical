    public boolean Checkvalues(int i){
        for(int k = 0; k<=i; k++){
            if(i != k){
                int int1 = btnval[i];
                int int2 = btnval[k];
                if(int1 == int2){
                        //JOptionPane.showMessageDialog(null, "Voltou");
                        return true;
                }
            }
        }
            //JOptionPane.showMessageDialog(null, "SAiu");
        return false;
    }
    
    public void AtribuiValor(int i){
        boolean resp = true;
        while(resp){
            int val = r.nextInt(12);
            btnval[i] = values[val]; 
            resp = Checkvalues(i);
        }
        
        
        
    }
    
