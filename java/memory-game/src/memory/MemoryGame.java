    public void Cards(int num){
        int g = btnval[num-1];
        //JOptionPane.showMessageDialog(null, g);
        ShowValues(false,num);
        if(c1 == false){
            x = g;
            card1 = num;
            c1 = true;
        }
        else if( c2 == false){
            y =g;
            card2 = num;
            c2 = true;
        }
        if(c1 && c2){
            CheckCards();
        }
    }
    
    
    public void NewGame() {
        pontos=0;
        ShowValues(true,0);
        ButtonEnabled(true,0);
        for(int i = 0; i<=11; i++){
            AtribuiValor(i);
        }
        
        for(int i = 0; i <=11; i++){
            if(btnval[i]>=10){
                btnval[i] = btnval[i]/10;
            }
        }
        
        
            
    }
    
